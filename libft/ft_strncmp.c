#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t	i;
	int		diff;

	i = 0;
	while (i < n)
	{
		diff = (unsigned char)s1[i] - (unsigned char)s2[i];
		if (diff != 0 || !s1[i] || !s2[i])
			return (diff);
		i++;
	}
	if (i == n)
		return (0);
	return (diff);
}
